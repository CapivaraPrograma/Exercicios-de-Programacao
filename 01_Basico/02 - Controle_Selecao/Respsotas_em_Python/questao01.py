primeiroNumero = int(input("Digite um número: "))
segundoNumero = int(input("Digite outro número: "))

print(f"Soma:           {primeiroNumero} + {segundoNumero} = {primeiroNumero + segundoNumero}")
print(f"Subtração:      {primeiroNumero} - {segundoNumero} = {primeiroNumero - segundoNumero}")
print(f"Multiplicação:  {primeiroNumero} * {segundoNumero} = {primeiroNumero * segundoNumero}")
print(f"Divisão:        {primeiroNumero} / {segundoNumero} = {primeiroNumero / segundoNumero}")
print(f"Resto:          {primeiroNumero} % {segundoNumero} = {primeiroNumero % segundoNumero}")
