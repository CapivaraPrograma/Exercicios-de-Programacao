fun main () {
    val primeiroNumero = 20
    val segundoNumero: Int = 3
    println("Soma:		$primeiroNumero + $segundoNumero = ${primeiroNumero + segundoNumero}")
    println("Subtração: 	$primeiroNumero - $segundoNumero = ${primeiroNumero - segundoNumero}")
    println("Multiplicação:  $primeiroNumero * $segundoNumero = ${primeiroNumero * segundoNumero}")
    println("Divisão:	$primeiroNumero / $segundoNumero = ${primeiroNumero / segundoNumero}")
    println("Resto: 		$primeiroNumero % $segundoNumero = ${primeiroNumero % segundoNumero}")   
}